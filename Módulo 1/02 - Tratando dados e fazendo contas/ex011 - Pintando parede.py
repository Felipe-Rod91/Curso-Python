larg = float(input('Qual a largura da parede em metros?'))
alt = float(input('E qual a altura dela em metros?'))
print('Se a largura é de {} metros e a altura é de {} metros, então a área dela é {}m², então precisamos de {} litros de tinta para pintá-la inteira.'.format(larg, alt, larg * alt, (larg * alt) / 2))
